// Extracts a structured inventory of the Figma KIT (node 75:2963) from the
// pulled nodes.json — every COMPONENT_SET (component + its states), plus the
// CAS STUDY / Notre vision frames. Outputs a readable Markdown reference to
// .design/figma-data/kit-inventory.md so we can build the design-system
// components pixel-perfect without re-hitting Figma.
//
// Run: node .design/scripts/kit-inventory.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "../..");
const data = JSON.parse(
	readFileSync(resolve(root, ".design/figma-data/nodes.json"), "utf8"),
);
const kit = data.nodes["75:2963"].document;

const hex = (c) => {
	if (!c) return null;
	const to = (v) => Math.round(v * 255).toString(16).padStart(2, "0");
	const base = `#${to(c.r)}${to(c.g)}${to(c.b)}`;
	return c.a != null && c.a < 1 ? `${base} @${c.a.toFixed(2)}` : base;
};

const solid = (paints) => {
	if (!Array.isArray(paints)) return null;
	const out = [];
	for (const p of paints) {
		if (p.visible === false) continue;
		if (p.type === "SOLID") out.push(hex({ ...p.color, a: p.opacity ?? p.color?.a }));
		else out.push(`<${p.type}>`);
	}
	return out.length ? out.join(", ") : null;
};

const radius = (n) => {
	if (n.rectangleCornerRadii)
		return n.rectangleCornerRadii.join("/");
	if (n.cornerRadius != null) return String(n.cornerRadius);
	return null;
};

const layout = (n) => {
	if (!n.layoutMode || n.layoutMode === "NONE") return null;
	const pad = [n.paddingTop, n.paddingRight, n.paddingBottom, n.paddingLeft]
		.map((v) => v ?? 0)
		.join("/");
	return `${n.layoutMode} gap=${n.itemSpacing ?? 0} pad=${pad} align=${n.primaryAxisAlignItems ?? "-"}/${n.counterAxisAlignItems ?? "-"}`;
};

const textInfo = (n) => {
	const s = n.style || {};
	const parts = [];
	parts.push(`"${(n.characters || "").replace(/\n/g, "⏎")}"`);
	parts.push(
		`${s.fontFamily || "?"} ${s.fontWeight || "?"} ${s.fontSize || "?"}px`,
	);
	if (s.letterSpacing) parts.push(`ls=${Number(s.letterSpacing).toFixed(2)}`);
	if (s.lineHeightPx) parts.push(`lh=${Math.round(s.lineHeightPx)}px`);
	if (s.textCase && s.textCase !== "ORIGINAL") parts.push(`case=${s.textCase}`);
	if (s.textAlignHorizontal) parts.push(`align=${s.textAlignHorizontal}`);
	const fill = solid(n.fills);
	if (fill) parts.push(`fill=${fill}`);
	const stroke = solid(n.strokes);
	if (stroke) parts.push(`STROKE=${stroke} ${n.strokeWeight ?? "?"}px`);
	// Per-character style overrides (e.g. first word in outline).
	if (n.characterStyleOverrides && n.styleOverrideTable) {
		const segs = new Set(n.characterStyleOverrides.filter(Boolean));
		for (const id of segs) {
			const ov = n.styleOverrideTable[id] || {};
			const bits = [];
			if (ov.fills) bits.push(`fill=${solid(ov.fills)}`);
			if (ov.strokes) bits.push(`STROKE=${solid(ov.strokes)} ${ov.strokeWeight ?? "?"}px`);
			if (ov.fontFamily) bits.push(ov.fontFamily);
			if (ov.fontWeight) bits.push(`w${ov.fontWeight}`);
			if (bits.length) parts.push(`[charOverride#${id}: ${bits.join(" ")}]`);
		}
	}
	return parts.join(" · ");
};

const lines = [];
const out = (s = "") => lines.push(s);

// Walk a node subtree, printing a compact indented description.
const walk = (n, depth) => {
	const pad = "  ".repeat(depth);
	const bb = n.absoluteBoundingBox;
	const dim = bb ? `${Math.round(bb.width)}×${Math.round(bb.height)}` : "";
	if (n.type === "TEXT") {
		out(`${pad}- TEXT ${textInfo(n)}`);
		return;
	}
	const meta = [];
	if (dim) meta.push(dim);
	const r = radius(n);
	if (r) meta.push(`radius=${r}`);
	const fill = solid(n.fills);
	if (fill) meta.push(`fill=${fill}`);
	const stroke = solid(n.strokes);
	if (stroke) meta.push(`stroke=${stroke} ${n.strokeWeight ?? "?"}px`);
	const lo = layout(n);
	if (lo) meta.push(lo);
	out(`${pad}- [${n.type}] "${n.name}" ${meta.join(" · ")}`);
	for (const c of n.children || []) walk(c, depth + 1);
};

out(`# KIT inventory — node 75:2963 (${kit.name})`);
out("");
out(`Source: Figma Rv5HxXNkF6VkTke0ttdAbe · KIT ${Math.round(kit.absoluteBoundingBox.width)}×${Math.round(kit.absoluteBoundingBox.height)}`);
out("Generated by .design/scripts/kit-inventory.mjs — do not edit by hand.");
out("");
out("⚠️ This is a MAP (what components exist), NOT a pixel-perfect build spec — it");
out("drops geometry, layer opacity, and layout. Before building a component, read its");
out("FULL node: `node .design/scripts/figma-node.mjs <nodeId|name>`. See the estuaire-figma skill.");
out("");

// Focus on the real components: COMPONENT_SET (with variants) and the named
// CAS STUDY / Notre vision frames + the multi-child groups.
const interesting = (kit.children || []).filter(
	(c) =>
		c.type === "COMPONENT_SET" ||
		c.type === "FRAME" ||
		(c.type === "GROUP" && (c.children || []).length > 2),
);

for (const node of interesting) {
	out("");
	out(`## ${node.type} — ${node.name}`);
	walk(node, 0);
}

const outPath = resolve(root, ".design/figma-data/kit-inventory.md");
writeFileSync(outPath, lines.join("\n"));
console.log(`Wrote ${outPath} (${lines.length} lines)`);
